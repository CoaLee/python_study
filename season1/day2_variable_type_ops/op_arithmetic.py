# 수 연산자 연습

# 값으로 바로 연산
print(192 + 411)
print(192 - 411)
print(192 * 411)
print(411 / 192)
print(411 // 192)
print(411 % 192)

# 변수 활용 연산
numA = 192
numB = 411
print(numA + numB)
print(numA - numB)
print(numA * numB)
print(numB / numA)
print(numB // numA)
print(numB % numA)

