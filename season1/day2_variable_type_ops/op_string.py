# 문자열 연산자 연습

# 값으로 바로 연산
print("안녕하세요 저는" + '하랑' + "입니다")
print('당신이 가장 좋아하는' + "음식" + "은 무엇인가요?")
print("안녕" * 4)
print("안녕\n" * 5)

# 변수 활용 연산
name = "하랑"
category = "음식"
msg = "잘가"

print("안녕 내 이름은 " + name + "라고 해")
print("제일 좋아하는 " + category + "가 뭐니?")
print(msg * 3)
print(msg + "\n" * 3)
print((msg + "\n") * 3)

