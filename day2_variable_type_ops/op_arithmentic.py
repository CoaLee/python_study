# 수 연산자 연습

# 값으로 바로 연산
print(190 + 411)
print(190 - 411)
print(190 * 411)
print(411 / 190)
print(411 // 190)
print(411 % 190)

# 변수 활용 연산
numA = 190
numB = 411
print(numA + numB)
print(numA - numB)
print(numA * numB)
print(numB / numA)
print(numB // numA)
print(numB % numA)

# 변수에 들어간 값 바꾸기
